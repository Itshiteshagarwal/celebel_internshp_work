//types of operators in javascript
// 1. Arithmetic Operators
// 2. Assignment Operators
// 3. Comparison Operators
// 4. Logical Operators
// 5. string concat operators
// 6. conditional operator


//1. Arithmetic operator (addition(+), subtraction(-), multiplication(*), division(/), modulus(%), increment(++), decrement(--), exponentiation(**))
//for example
let a = 3;
let x = (100 + 50) * a;
console.log(x);

//2. assignment operator (=,+=,-=,*=,/=,%=,**=)
//for example
let y = 10;
y += 5;
console.log(y);

//3. comparison operator(==,===,!=,!==,>,<,>=,<=)
//for example
let text1 = "20";
let text2 = "5";
let result = text1 < text2;
console.log(result);

//4. logical operators(&&,||,!)
//for example
// (x < 10 && y > 1) is true;

//5.string concat operator(+)
let message = "hello!!";
let name = "hitesh agarwal";
let concat = message+name;
console.log(concat);

//6. conditional operator(?, :)
//for example
status = (marks >= 30) ? "Pass" : "Fail"

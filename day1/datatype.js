//examples of primitive datatype
// Number:
let age = 25;
let price = 9.99;

//String:
let name = "John Doe";
let message = 'Hello, World!';

//Boolean:
let isTrue = true;
let isFalse = false;


//example of composite datatype
// Object:
let person = {
    name: "John",
    age: 30,
    isEmployed: true
  };

//array:
let numbers = [1, 2, 3, 4, 5];
let fruits = ['apple', 'banana', 'orange'];

//function:
function add(a, b) {
    return a + b;
  }
  